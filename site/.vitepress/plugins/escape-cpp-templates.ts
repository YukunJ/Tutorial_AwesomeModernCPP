  'h1','h2','h3','h4','h5','h6','head','header','hgroup','hr','html',
  'i','iframe','img','input','ins','kbd','label','legend','li','link',
  'main','map','mark','menu','meta','meter','nav','noscript',
  'object','ol','optgroup','option','output','p','picture','pre','progress',
  'q','rp','rt','ruby','s','samp','script','section','select','slot',
  'small','source','span','strong','style','sub','summary','sup',
  'table','tbody','td','template','textarea','tfoot','th','thead',
  'time','title','tr','track','u','ul','var','video','wbr',
  'client-only','content','doc-footer','doc-sidebar',
  'mjx-container', 'mjx-assistive-mml', 'mjx-body', 'mjx-math', 'mjx-mrow', 'mjx-mi', 'mjx-mo', 'mjx-mn', 'mjx-msup', 'mjx-msub', 'mjx-mfrac', 'mjx-munder', 'mjx-mover', 'mjx-mspace', 'mjx-mtext', 'mjx-ms', 'mjx-mpadded', 'mjx-mphantom', 'mjx-menclose', 'mjx-merror',
